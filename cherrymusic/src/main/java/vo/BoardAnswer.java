package vo;

public class BoardAnswer {
	private int boardANo;
	private int boardQNo;
	private String id;
	private String boardAContent;
	private String createdate;
	private String updatedate;
	public int getBoardANo() {
		return boardANo;
	}
	public void setBoardANo(int boardANo) {
		this.boardANo = boardANo;
	}
	public int getBoardQNo() {
		return boardQNo;
	}
	public void setBoardQNo(int boardQNo) {
		this.boardQNo = boardQNo;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getBoardAContent() {
		return boardAContent;
	}
	public void setBoardAContent(String boardAContent) {
		this.boardAContent = boardAContent;
	}
	public String getCreatedate() {
		return createdate;
	}
	public void setCreatedate(String createdate) {
		this.createdate = createdate;
	}
	public String getUpdatedate() {
		return updatedate;
	}
	public void setUpdatedate(String updatedate) {
		this.updatedate = updatedate;
	}
	
	
}
